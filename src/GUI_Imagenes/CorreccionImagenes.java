    private void procesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_procesarActionPerformed
        dispose();
        proceso = new Procesando(this.principal);
        this.principal.add(proceso);
        HiloProceso.active = true;
        HiloProceso h = new HiloProceso(proceso,this.listaIm,1);
        h.darProcesando(proceso);
        h.pasarProceso(this.nProceso,nombre,grupo,nombreProceso);
        h.forma(forma);
        h.start();
    }//GEN-LAST:event_procesarActionPerformed

    public void muestra(){
        Panel.setLayout(new GridLayout((int)(rutas.size()/2),2));
        BufferedImage leerImagen;
        File extension;
        
        for(int i=0; i<rutas.size(); i++){
            try {
                extension = new File(rutas.get(i));
                leerImagen = ImageIO.read(extension);
                boton = new JButton();
                
                boton.setContentAreaFilled(false);
                boton.setHorizontalTextPosition(SwingConstants.CENTER);
                boton.setVerticalTextPosition(SwingConstants.BOTTOM);
                int g = (Panel.getWidth())-((35*Panel.getWidth())/100);
                int g2 = (Panel.getWidth())-((26*Panel.getWidth())/100);
                boton.setRolloverIcon(new ImageIcon(leerImagen.getScaledInstance((int) (g2/1.5), 
                        (int) (g2/1.5),Image.SCALE_DEFAULT)));
                boton.setIcon(new ImageIcon(leerImagen.getScaledInstance(g/2,
                        g/2,Image.SCALE_DEFAULT)));
                Panel.add(boton);
                Panel.updateUI();
                
            } catch (IOException ex) {
                Logger.getLogger(CorreccionImagenes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
